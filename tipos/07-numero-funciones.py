import math

print(round(1.3)) #round : devuelve el numero mas cercano entero
print(round(1.7))
print(abs(-77)) #abs : valor absoluto del numero que le pasemos siempre numero positivo
print(abs(55)) #abs : valor absoluto del numero que le pasemos siempre numero positivo


print(math.ceil(1.1)) #ceil : va a llevar el numero al numero siguiente mas cercano ENTERO
print(math.floor(1.99)) #floor : toma el numero y lo lleva al numero entero que esta abjo
print(math.isnan("23")) #isnan : si corresponde a no es un numero devuelve un boolean
print(math.pow(10, 3)) #pow : Eleva el numero a su potencia
print(math.sqrt(9)) #sqrt : Raiz cuadrada del numero
